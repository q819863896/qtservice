//////////////////////////////////////////////////////////////////////////////系统超时提示//////////////////////////////////////////////////////////////////////////////////////////////
export const systemTimeout = function (status, message) {
   if (status === "" || status === undefined || status === null) {
      Message({type: "error", message: "系统登录超时!"})
   } else {
      Message({
         duration: 5000,
         showClose: true,
         message: message,
         type: "error"
      });
   }
};

